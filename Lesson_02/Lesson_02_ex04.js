'use strict';
/**
 * Функция складывает параметры
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function getSum(a, b) {
    return a + b
}


/**
 * Функция вычитает параметры
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function getSub(a, b) {
    return a - b
}


/**
 * Функция умножает параметры
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function getMult(a, b) {
    return a * b
}


/**
 * Функция выполняет деление параметров
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function getDivision(a, b) {
    return a / b
}